print(10 + 3) #Addition
print(10 - 3) #Substraction
print(10 * 3) #Multiplication
print(10 / 3) #Division (returns float)
print(10 // 3) #Division (returns int)
print(10 % 3) #Modulus? (returns remainder of the division)
print(10 ** 3) #Exponent

